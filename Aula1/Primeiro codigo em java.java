public class Main {
  public static void main(String[] args) {

   int a=4, b=2;
  calculadora (a,b);
  }
  
    public static void calculadora(int a, int b)
  {
    System.out.println("\n A+B="+(a+b));//printf
    System.out.println(" A-B="+(a-b));//sub
    System.out.println(" A*B="+(a*b));//mult
    System.out.println(" A/B="+(a/b));//divisao
    System.out.println(" A%B="+(a%b));//resto
  }
}
